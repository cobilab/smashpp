#define __STDC_FORMAT_MACROS
#if defined(_MSC_VER)
#include <io.h>
#else
#include <unistd.h>
#endif

#include "application.hpp"

int main(int argc, char* argv[]) {
  smashpp::enable_fast_io();

  try {
    const auto t0{smashpp::now()};
    smashpp::application{}.exe(argc, argv);
    const auto t1{smashpp::now()};
    std::cerr << "Total time: " << smashpp::hms(t1 - t0);
  } catch (std::exception& e) {
    std::cerr << e.what();
  } catch (...) {
    return EXIT_FAILURE;
  }

  return 0;
}